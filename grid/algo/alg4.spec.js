const f = require("./algo4.js");

describe(":: Сумматор с отложенным вызовом (add(9)(10)())", () => {
  it("1️⃣ должен возвращать 0 при вызове без аргументов: f()", () => {
    expect(f()).toBe(0);
  });

  it("2️⃣ должен возвращать число при одном аргументе: f(9)()", () => {
    expect(f(9)()).toBe(9);
  });

  it("3️⃣ должен правильно суммировать два числа: f(10)(9)()", () => {
    expect(f(10)(9)()).toBe(19);
  });

  it("4️⃣ должен поддерживать длинную цепочку вызовов", () => {
    expect(f(1)(2)(3)(4)(5)()).toBe(15);
  });

  it("5️⃣ должен корректно работать с отрицательными числами", () => {
    expect(f(-5)(10)(-3)(7)()).toBe(9);
  });

  it("6️⃣ должен правильно обрабатывать нули", () => {
    expect(f(0)(0)(0)(10)()).toBe(10);
    expect(f(5)(0)(0)()).toBe(5);
  });

  it("7️⃣ должен работать с большими суммами", () => {
    expect(f(100)(200)(300)(400)(500)()).toBe(1500);
  });

  it("8️⃣ должен корректно суммировать смешанные положительные и отрицательные значения", () => {
    expect(f(10)(-5)(3)(-8)(20)()).toBe(20);
  });
});
